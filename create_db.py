def main() -> None:
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute("DROP TABLE IF EXISTS plants")
    cursor.execute(
        """
        CREATE TABLE plants (
            plant_id INTEGER PRIMARY KEY,
            scientific_name TEXT NOT NULL,
            image_url TEXT,
            russian_name TEXT,
            osetian_name TEXT,
            general_info TEXT,
            cultural_info TEXT
        )
        """
    )
    cursor.executemany(
        """
        INSERT INTO plants (
            plant_id, scientific_name, image_url, russian_name,
            osetian_name, general_info, cultural_info
        ) VALUES (?, ?, ?, ?, ?, ?, ?)
        """,
        PLANTS,
    )
    conn.commit()
    conn.close()
    print(f"База данных создана: {DB_PATH}")


if __name__ == "__main__":
    main()
